package operator_and_for_loop;

public class Assignment_operator {
	
	public static void main(String x[]) {
		
		int a = 10;
		//a=a+1;
		a+=1; //these are same
		System.out.println(a);
		a-=1;
		System.out.println(a);
		System.out.println(a*=2);
		System.out.println(a/=2);
		System.out.println(a^=1);
		System.out.println(a<<=1);
		
	}
}
